function varargout = PsgDirect_OutputFcn(hObject, eventdata, handles) 
% varargout  cell array for returning output args (see VARARGOUT);
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Get default command line output from handles structure
varargout{1} = handles.output;

X=str2double(get(handles.edit1,'String'));
Y=str2double(get(handles.edit2,'String'));
Z=str2double(get(handles.edit3,'String'));

%On va utiliser les données de l'ellipsoide de clarke 1880
%Données
a=6378249.2;
f=1/293.4663;
b=a*(1-f);
e2=(2*f)-(f^2);
rad=180/pi;

r=sqrt((X^2)+(Y^2)+(Z^2));
u=(atan((Z/sqrt(X^2+Y^2))*((1-f)+(a*e2)/r)))*rad;
%longitude 
LANDA=(atan(Y/X))*rad;
%latitude géodésique
PHI=atan((Z*(1-f)+e2*a*(sin(u/rad)^3))/((1-f)*sqrt(X^2+Y^2)-e2*a*(cos(u/rad)^3)))*rad; 
%élévation ellipsoidale
H=(sqrt(X^2+Y^2)*cos(PHI/rad))+(Z*sin(PHI/rad))-(a*sqrt(1-e2*(sin(PHI/rad)^2)));

LANDA= abs(LANDA);

LANDA1= floor(LANDA);
LANDA2= floor((LANDA-LANDA1)*60);
LANDA3=(LANDA-LANDA1-(LANDA2/60))*3600;

PHI=abs(PHI);

PHI1=floor(PHI);
PHI2= floor((PHI-PHI1)*60);
PHI3= floor(PHI-PHI1-(PHI2/60))*3600;

set(handles.edit4,'String',PHI1);
set(handles.edit5,'String',PHI2);
set(handles.edit6,'String',PHI3);
set(handles.edit7,'String',LANDA1);
set(handles.edit8,'String',LANDA2);
set(handles.edit9,'String',LANDA3);
set(handles.edit10,'String',H);



